    private void initialisation()
    {
        this.parametres = new Parametres();
        this.NumLocal.setValue(this.parametres.get_num_serveur_local());
        this.PortLocal.setText(""+this.parametres.get_port_serveur_local());
        this.LoginBD.setText(this.parametres.getBD_login());
        this.MdpBD.setText(this.parametres.getBD_mdp());
        this.nb_serveurs = this.parametres.get_nb_serveurs();
        this.num_serveurs = new ArrayList<>();
        for(int i=0; i<this.nb_serveurs; i++)
        {
            this.num_serveurs.add(this.parametres.get_num_serveur(i));
        }
        this.remplissageServeurs();
    }
    
    private void remplissageServeurs()
    {
        this.Serveurs.removeAll();
        this.Serveurs.setLayout(new GridLayout(this.nb_serveurs-1, 3));
        for(int i=0; i<this.nb_serveurs; i++)
        {
            if(this.num_serveurs.get(i)!=this.parametres.get_num_serveur_local())
            {
                //Ne pas ajouter le serveur si c'est le local
                JLabel jl = new JLabel();
                jl.setText("Serveur "+(i+1)+" : ");
                SpinnerModel model = new SpinnerNumberModel();
                JSpinner js = new JSpinner(model);
                js.setValue(this.num_serveurs.get(i));
                js.setName("js"+i);
                JButton jb = new JButton();
                jb.setText("Supprimer");
                jb.setName("jb"+i);
                //Ajout du listener
                jb.addActionListener(new java.awt.event.ActionListener() 
                {
                    public void actionPerformed(java.awt.event.ActionEvent evt) {
                        supprimerServeur(evt);
                    }
                });
                this.Serveurs.add(jl);
                this.Serveurs.add(js);
                this.Serveurs.add(jb);
            }
        }
    }
    
    private void supprimerServeur(java.awt.event.ActionEvent evt)
    {
        JButton jb = (JButton)evt.getSource();
        int num = Integer.parseInt(jb.getName().split("jb")[0]);
    }
    
